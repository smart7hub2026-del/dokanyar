export function countGapsByLevel(catalog: GapCategory[]) {
  let high = 0;
  let medium = 0;
  let info = 0;
  for (const c of catalog) {
    for (const i of c.items) {
      if (i.level === 'high') high += 1;
      else if (i.level === 'medium') medium += 1;
      else info += 1;
    }
  }
  const total = high + medium + info;
  return { high, medium, info, total };
}
